package ObjetosSerializables;

/**
 * 
 * @author luiiis
 *
 */
public class Tipo {

	String idtipo;
	String tipo;

	/**
	 * 
	 * @param tipo
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	/**
	 * 
	 * @return
	 */
	public String getTipo() {
		return this.tipo;
	}

	/**
	 * 
	 * @param idtipo
	 */
	public void setIdtipo(String idtipo) {
		this.idtipo = idtipo;
	}

	/**
	 * 
	 * @return
	 */
	public String getIdtipo() {
		return this.idtipo;
	}
}
